package me.cbhud.entiteti;

public class tipVoza {
	
	int id;
	
	String nazivTipa;
	public tipVoza(int id, String nazivTipa) {
		super();
		this.id = id;
		this.nazivTipa = nazivTipa;
	}
	public int getId() {
		return id;
	}
	public void setId(int id) {
		this.id = id;
	}
	public String getNazivTipa() {
		return nazivTipa;
	}
	public void setNazivTipa(String nazivTipa) {
		this.nazivTipa = nazivTipa;
	}
	
	
	
	

}
